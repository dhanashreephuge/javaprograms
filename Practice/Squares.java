public class Squares {
    public static void main(String[] args) {
        // Loop through numbers from 1 to 5
        for (int i = 1; i <= 5; i++) {
            // Calculate the square using multiplication
            int square = i * i;
            // Print the result
            System.out.println("Square of " + i + " is " + square);
        }
    }
}
